    STATUS            VARCHAR2(50),
    EXPIRED_AT        DATE,
    USED_AT           DATE,
    EXCHANGED_AT      DATE DEFAULT SYSDATE,
    CONSTRAINT FK_LQ_REWARD_EXCHANGE_USER
        FOREIGN KEY (USER_ID) REFERENCES LQ_USER(USER_ID),
