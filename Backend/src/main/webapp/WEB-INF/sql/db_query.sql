CREATE TABLE LQ_RECEIPT (
    RECEIPT_ID               NUMBER PRIMARY KEY,
    USER_ID                  NUMBER NOT NULL,
    USER_QUEST_PROGRESS_ID   NUMBER NOT NULL,
    CATEGORY                 VARCHAR2(50) NOT NULL,
    VERIFY_STATUS            VARCHAR2(50) NOT NULL,
    FILE_PATH                VARCHAR2(4000) NOT NULL,
    FILE_UPLOAD_NAME         VARCHAR2(100),
    FILE_ORIGINAL_NAME       VARCHAR2(100),
    CREATED_AT               DATE DEFAULT SYSDATE NOT NULL,

    CONSTRAINT FK_LQ_RECEIPT_USER
        FOREIGN KEY (USER_ID) REFERENCES LQ_USER(USER_ID),

    CONSTRAINT FK_LQ_RECEIPT_UQP
        FOREIGN KEY (USER_QUEST_PROGRESS_ID)
        REFERENCES LQ_USER_QUEST_PROGRESS(USER_QUEST_PROGRESS_ID),

    CONSTRAINT FK_LQ_RECEIPT_CATEGORY
        FOREIGN KEY (CATEGORY)
        REFERENCES LQ_QUEST_CATEGORY(CATEGORY_NAME),

    CONSTRAINT CHK_LQ_RECEIPT_VERIFY_STATUS
        CHECK (VERIFY_STATUS IN ('SUCCESS', 'FAILED'))
);

